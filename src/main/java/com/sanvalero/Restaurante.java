
package com.sanvalero;

/**
 *
 * @author María Carmen Jiménez Sampériz
 */
public class Restaurante {
    private String nombreRestaurante;
    private String direccionResta;
    private int numeroMesas;

    public Restaurante() {
    }

    public String getNombreRestaurante() {
        return nombreRestaurante;
    }

    public void setNombreRestaurante(String nombreRestaurante) {
        this.nombreRestaurante = nombreRestaurante;
    }

    public String getDireccionResta() {
        return direccionResta;
    }

    public void setDireccionResta(String direccionResta) {
        this.direccionResta = direccionResta;
    }

    public int getNumeroMesas() {
        return numeroMesas;
    }

    public void setNumeroMesas(int numeroMesas) {
        this.numeroMesas = numeroMesas;
    }

    
    
}
